def create_grid_with_pattern(
        pattern: np.ndarray, grid_width: int = 50, grid_height: int = 50
) -> np.ndarray:
    """
    Create a grid with a given pattern (e.g. glider, beacon) in the middle.
    
    Parameters
    ----------
    pattern : numpy.ndarray
        A 2-dimensional binary array representation of the pattern.
    grid_width : int
        Number of cells in a row of the grid.
    grid_height : int
        Number of cells in a column of the grid.
    
    Returns
    -------
    grid : numpy.ndarray
        A grid with the given dimensions and the pattern in the middle (otherwise empty). 
    """
    grid = np.zeros(shape=(grid_width, grid_height), dtype=np.bool_)
    pattern_dims = np.array(pattern.shape)
    start = np.array([grid_width, grid_height]) // 2 - pattern_dims // 2
    end = start + pattern_dims
    grid[start[0]:end[0], start[1]:end[1]] = pattern
    return grid


def create_grid_random(grid_width: int = 50, grid_height: int = 50) -> np.ndarray:
    return np.random.randint(low=0, high=2, size=(grid_width, grid_height), dtype=np.bool_)
