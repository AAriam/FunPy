def create_grid_with_pattern(pattern, grid_width=50, grid_height=50):
    """
    Take a pattern (e.g. glider, beacon) and create a grid with the pattern in the middle.
    
    Parameters
    ----------
    pattern : numpy array (2-dimensional, binary)
        Array representation of the pattern.
    grid_width : int
        Number of cells in a row of the grid.
    grid_height : int
        Number of cells in a column of the grid.
    
    Returns
    -------
    grid : numpy array (2-dimensional, binary)
        A grid with the given dimensions and the pattern in the middle (otherwise empty). 
    """
    
    grid = np.zeros((grid_width, grid_height))
    
    pattern_width = pattern.shape[0]
    pattern_height = pattern.shape[1]
    
    start_col = grid_width//2 - pattern_width//2
    start_row = grid_height//2 - pattern_height//2
    
    end_col = start_col + pattern_width
    end_row = start_row + pattern_height
    
    grid[start_col:end_col, start_row:end_row] = pattern
    
    return grid